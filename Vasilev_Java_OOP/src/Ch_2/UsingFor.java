package Ch_2;

class UsingFor{
    public static void main(String[] args){
// Индексная переменная:
        int i;
// Переменная для вычисления суммы:
        int sum=0;
// Инструкция цикла:
        for(i=1;i<=100;i++){
            sum+=i;}
        System.out.println("Сумма чисел от 1 до 100: "+sum);}
}
