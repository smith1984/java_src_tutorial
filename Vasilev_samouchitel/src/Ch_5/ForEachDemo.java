package Ch_5;

public class ForEachDemo{
    public static void main(String[] args){
// Исходный массив для отображения:
        int[] a=new int[]{1,2,4,7,3,1,8,9,2};
// Упрощенная форма оператора цикла:
        for(int s:a){
            System.out.print(s+" ");}
        System.out.println();}}