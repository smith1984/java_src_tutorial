public class CharArithDemo {
    public static void main (String [] args)
    {
        char ch;
        ch ='L';

        System.out.println("ch равно " + ch);

        ch ++;
        System.out.println("ch равно " + ch);

        ch = 'Д';
        System.out.println("ch равно " + ch);

        ch--;
        System.out.println("ch равно " + ch);
    }//main()
}// CharArithDemo
