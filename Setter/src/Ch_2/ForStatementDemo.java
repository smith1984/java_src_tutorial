public class ForStatementDemo {
    public static  void main (String [] args){
        int count;
        for (count = 0; count < 5; count++)
            System.out.println("Переменная цикла равна " + count);
        System.out.println("Цикл завершен");
    }//main()
}//ForStatementDemo
